USE OSUADV_PROD.BI;

CREATE OR REPLACE TABLE "DNRCNCT_Manager"
    (
        prospectID varchar(20)
        ,value varchar(1000)
    )
 ;

INSERT INTO "DNRCNCT_Manager"
SELECT
   C.CONSTITUENTID prospectID
  ,MGR.MANAGERLIST AS value

FROM 
	"OSUADV_PROD"."RE"."CONSTITUENT" C INNER JOIN "OSUADV_PROD"."RE"."MANAGEDCONSTITUENTS" MGR ON C.CONSTITUENTSYSTEMID = C.CONSTITUENTSYSTEMID
    INNER JOIN "OSUADV_PROD"."BI"."DNRCNCT_InclLst_minus_ExclLst" IME ON IME.CONSTITUENTSYSTEMID = MGR.CONSTITUENTSYSTEMID
	
GROUP BY C.CONSTITUENTID
